$S_Desc_riskAssessment='Die qualitative Risikobewertung ermöglicht es, schnell potenzielle Risiken, sowie deren Eigenschaften und Ursachen zu identifizieren. Die qualitative Risikobewertung beschäftigt sich mit dem Vergleich von Schadstoffkonzentrationen, die in Boden, Wasser oder Bodenluft am Standort gemessen wurden, mit allgemeinen Bewertungskriterien. Allgemeine Bewertungskriterien sind üblicherweise konservativ, um sicherzustellen, dass sie auf die Mehrzahl der unterschiedlichen Standorte anwendbar sind, und sie sind normalerweise nur für eine begrenzte Anzahl von Schadstoffen anwendbar. Die quantitative Risikobewertung erlaubt eine größere Nutzung von standortspezifischen Daten, um eine exaktere Bewertung der Risiken durchführen zu können. Die quantitative (detaillierte) Risikobewertung schließt die Nutzung von Modellen für die Ableitung standortspezifischer Bewertungskriterien ein, die dann mit den gemessenen Konzentrationen in Boden, Wasser oder Bodenluft am Standort verglichen werden, um die Risiken einschätzen zu können.';
