<h2>Einführung</h2>
<p>
    Seit der zweiten Hälfte des 19. Jahrhunderts haben Industrialisierung und der wirtschaftliche Umbruch dazu geführt, dass überall in Europa viele <u id="brownfield">Brachflächen (BF)</u>  entstanden.
</p>
<p> 
    Um die Herausforderung der BF-Revitalisierung anzugehen, wurden viele Netzwerke und Projekte initiiert. Diese Projekte haben eine große Anzahl von Methoden und Frameworks (also konzeptionelle Rahmen), sowie Handbücher, Entscheidungshilfen, Leitfäden, Werkzeuge usw. hervorgebracht, deren praktische Anwendung jedoch hinter den Erwartungen blieb. Insbesondere wurden viele konzeptionelle, modellhafte Abläufe von idealisierten Prozessen entwickelt, die am Ende dennoch nicht alle Aspekte eines Revitalisierungsprozesses berücksichtigen. 
</p>
<p> 
    Das europäische Forschungsprojekt TIMBRE – Tailored Improvement for Brownfield Regeneration in Europe – (<a target="blank" href="http://www.timbre-project.eu">www.timbre-project.eu</a>) – zielt darauf ab, Endnutzer zu unterstützen durch eine Bereitstellung zweckmäßiger Ansätze und Management-Tools für die nutzerspezifischen Probleme bei der Nachnutzungsplanung und Sanierung von Brachflächen. 
</p>
<p> 
    Eines der Ergebnisse des TIMBRE-Projektes ist die Entwicklung eines umfassenden Analyserahmes für die Sammlung verfügbarer Informationen zur BF-Revitalisierung. Die Entwicklung des TIMBRE-Rahmens basiert auf einer Evaluationen bereits vorgeschlagener Frameworks vorangegangener Projekte. Zudem wurden verschiedene relevante Akteure befragt hinsichtlich der notwendigen Informationskategorien   
    (
<u class="c_sp">Strategische Planung</u>,
<u class="c_i">Erkundung (orientierende / detaillierte)</u>,
<u class="c_ra">Risikobewertung (qualitativ/quantitativ)</u>,
<u class="c_rs">Sanierungsstrategien und -optionen</u>,
<u class="c_rt">Evaluierung und Auswahl von Sanierungstechnologien</u>,
<u class="c_bi">Unterlagen zu Gebäuden und Infrastruktur</u>,
<u class="c_d">Rückbau/Nachnutzung von Bausubstanz und Materialien</u>,
<u class="c_w">Abfallmanagement</u>,
<u class="c_req">Nachnutzungsplanung/Sanierungsplanung</u>,
<u class="c_impl">Umsetzung, Überwachung, Monitoring (Wiedereingliederung in den Grundstücksverkehr)</u>,
<u class="c_sea">Sozio-ökonomische Bewertung</u>,
<u class="c_ff">Förderung und Finanzierung</u> und 
<u class="c_dm">Entscheidungsfindung und Kommunikation</u>

). Das TIMBRE-Framework für die Sammlung verfügbarer Informationen zur BF-Revitalisierung ist in Abb. 1 dargestellt.
