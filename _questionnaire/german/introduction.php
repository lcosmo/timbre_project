<h3>Ziel des TIMBRE-Frameworks</h3>
<p>
    Das TIMBRE-Frameworks zur Strukturierung und Sammlung verfügbarer Informationen zur BF-Revitalisierung ist eng verknüpft mit dem allgemeinen Zielstellungen des TIMBRE-Projektes, das die Bereitstellung maßgeschneiderter Lösungen für spezifische Probleme bei der BF-Revitalisierung mittels der Analyse, Klassifikation und Auswahl von Tools, Methoden, Technologien, Entscheidungshilfen, etc., die im Rahmen vorangegangener Projekte entwickelt wurden, zum Ziel hat, um deren praktische Nutzung anzutreiben. 
</p>
<p>
    Die speziellen Ziele des TIMBRE-Framework für die Sammlung verfügbarer Informationen zur BF-Revitalisierung sind Folgende:
<ul>
    <li>
        a)	die TIMBRE-Partner bei der Sammlung von relevanten Informationen zur BF-Revitalisierung (Gesetze/Verordnungen, Entscheidungshilfen, Tools, Verfahrensweisen, Technologien, etc.) zu unterstützen, um diese den Interessengruppen verfügbar zu machen;
    </li><li>
        b)	die Entwicklung eines Expertensystems zu unterstützen, das einen einfachen Zugang und intuitive Schnittstellen beim Suchen und Sammeln relevanter Informationen ermöglichen wird;
    </li><li>
        c)	die Interessengruppen, die in den Prozess der BF-Revitalisierung involviert sind, durch die Nutzung des TIMBRE-Expertensystems beim Finden der benötigten, relevanten Informationen zu jeder Phase des BF-Revitalisierungsprozesses zu unterstützen. 
    </li></ul>

<h3>Ziel des Fragebogens</h3>
<p>
    Die Einbeziehung der Endnutzer bzw. der Stakeholder in die Entwicklung des oben erwähnten TIMBRE-Framework ist wesentlich für die Effektivität und Effizienz des geplanten Tools, sowie Voraussetzung für die Erzielung eines gemeinsamen und einverständlichen Ergebnisses.
    In diesem Zusammenhang ist Ihre Mitarbeit sehr wichtig und von größtem Wert. Durch die Beantwortung des nachfolgenden Fragebogens würden wir gern Ihre Meinung zu dem vorgeschlagenen TIMBRE-Framework erheben. Ihre Resonanz und die von Ihnen zur Verfügung gestellten Vorschläge in der Befragung werden uns bei der Verbesserung des Frameworks und bei der Identifizierung wichtiger fehlender Informationskategorien unterstützen.
</p>
<p>
    Eine große Beteiligung bei der Evaluierung des Frameworks für die Sammlung verfügbarer Informationen zur BF-Revitalisierung ist uns wichtig, da eine große Anzahl von Informationsquellen verschiedener Herkunft bessere Chancen beim Finden neuer Ideen und neuer Lösungen und bei der Identifizierung möglicher Lücken besitzen.
    Zudem kann Ihre Beteiligung bei der Evaluierung des Frameworks eine verbesserte Plausibilität und Legitimität des Endergebnisses gewährleisten. 
    Schlussendlich kann Ihre Beteiligung sowohl bei der Verbesserung der Effektivität als auch bei der Verbesserung der Effizienz des geplanten Frameworks für die Sammlung verfügbarer Informationen zur BF-Revitalisierung im Rahmen des TIMBRE-Projektes beitragen. 
</p>