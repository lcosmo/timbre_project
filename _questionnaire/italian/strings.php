$S_Desc_investigation='Caratterizzazione (preliminare/di dettaglio): determinazione delle caratteristiche del sito e definizione della misura e dell’entità della contaminazione presente. L’indagine preliminare riguarda l’individuazione di una potenziale contaminazione in base alle informazioni sulla storia del sito (per esempio mappe, piani, fotografie, dati geologici e idrologici, proprietari/residenti passati, usi industriali o commerciali precedenti, materie prime impiegate, smaltimento dei rifiuti e attività minerarie di ogni tipo) e ai dati di campionamento disponibili. L’indagine di dettaglio si concentra sul confermare la potenziale contaminazione di un sito, determinare la sua ubicazione, caratterizzare la sua natura ed entità e definire il modello concettuale del sito. È indispensabile eseguire uno studio adeguatamente dettagliato del sito al fine di individuare le cause, la natura e l’estensione della contaminazione e le possibili minacce per l’ambiente e per le persone che eventualmente vivono o lavorano nelle vicinanze.';
$S_Desc_riskAssessment='Analisi di rischio (qualitativa/quantitativa): l’analisi qualitativa del rischio permette di identificare rapidamente i rischi potenziali, nonché le attività e le risorse vulnerabili a questi rischi. L’analisi qualitativa del rischio si occupa del confronto delle concentrazioni di contaminante misurate nel suolo, acqua, gas interstiziali in un sito con i criteri di valutazione generici. Tali criteri sono in genere conservativi, in modo da assicurare la loro applicabilità alla maggior parte dei siti e normalmente si applicano solo a un numero limitato di inquinanti. L’analisi quantitativa del rischio fa maggior uso di dati sito-specifici in modo da effettuare una più accurata valutazione del rischio. Tale valutazione prevede l’utilizzo di modelli per ricavare criteri di valutazione sito-specifici i quali vengono poi confrontati con le concentrazioni misurate nel suolo, nell’acqua e nei gas del suolo per stimarne il rischio.';
$S_Desc_remediationStrat='Strategie di bonifica e opzioni alternative: revisione ed analisi delle alternative di bonifica. Questa categoria è propedeutica alla selezione delle tecnologie di bonifica perché mira a raccogliere le informazioni disponibili sulle possibili strategie e opzioni che abbiano la capacità di soddisfare specifici obiettivi di bonifica e riqualificazione, in conformità ai requisiti di legge, alla pianificazione regionale/locale e agli obiettivi di sviluppo.';
$S_Desc_remediationTech='Valutazione e selezione delle tecnologie di bonifica: permette di valutare le varie tecnologie di bonifica al fine di identificare quelle con le capacità di soddisfare specifici obiettivi di risanamento e riqualificazione, tenendo conto anche degli aspetti economici (cioè le tecnologie di bonifica più idonee per il sito sotto esame in base a uno specifico budget). L’analisi, la revisione e la selezione delle alternative di bonifica si basa sui dati raccolti durante la valutazione del sito, le fasi di indagine e le analisi dei costi e dei benefici.';
