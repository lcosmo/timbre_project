<h3>Scopo del framework</h3>
<p>
    L’obiettivo principale del framework TIMBRE per la raccolta delle informazioni relative alla riqualificazione dei BFs è strettamente connesso con l’obiettivo generale del progetto TIMBRE, ossia quello di fornire soluzioni su misura per specifici problemi riguardanti i BFs attraverso l’analisi, la classificazione e la selezione di strumenti, approcci, tecnologie, sistemi di supporto alle decisioni, ecc., sviluppati nei precedenti progetti di ricerca, con lo scopo di stimolarne il loro utilizzo pratico.
</p>
<p>
    Gli obiettivi specifici del framework TIMBRE per la raccolta delle informazioni relative alla riqualificazione dei BFs sono i seguenti:
<ul>
    <li>
        a)	supportare i partner TIMBRE nella raccolta di informazioni sui processi di riqualificazione dei BFs (normativa/regolamenti, framework concettuali, sistemi di supporto alle decisioni, strumenti, metodologie, approcci, tecnologie, ecc.) al fine di renderle poi disponibili agli stakeholders;
    </li><li>
        b)	supportare i partner TIMBRE nello sviluppo di un sistema esperto che fornirà un facile accesso e un’interfaccia intuitiva per cercare e raccogliere informazioni utili a tutti i partner, gli utilizzatori finali e gli stakeholders;
    </li><li>
        c)	supportare gli stakeholders coinvolti nel processo di riqualificazione dei BFs nel trovare le informazioni rilevanti e necessarie per ogni fase del processo attraverso l’utilizzo del sistema esperto TIMBRE.
    </li></ul>

<h3>Scopo del questionario</h3>
<p>
    In virtù della sempre più crescente importanza del coinvolgimento degli stakeholders all’interno del processo di riqualificazione dei BFs, riteniamo che la Vostra partecipazione nello sviluppo del framework TIMBRE possa essere utile per migliorare l’efficacia di tale strumento e per raggiungere un risultato condiviso.
    In questo contesto, la Vostra collaborazione sarà molto importante e apprezzata. Attraverso la compilazione del seguente questionario vorremmo raccogliere le Vostre opinioni sul framework TIMBRE per la raccolta delle informazioni relative alla riqualificazione dei BFs.
</p>
<p>
    Un’ampia partecipazione nella valutazione del framework porterà benefici utili a tutti gli attori coinvolti, poiché un numero elevato di fonti di informazione con background differenti ha migliori possibilità di sviluppare nuove idee e soluzioni utili al miglioramento del framework, così come di individuare eventuali e possibili mancanze.
    Inoltre, la partecipazione alla valutazione del framework accrescerà la conoscenza e la consapevolezza tra tutti gli attori coinvolti nel processo di riqualificazione dei BFs, nonché la credibilità e la legittimità del framework proposto.
    Per concludere, la Vostra partecipazione può contribuire a migliorare sia l’efficacia che l’efficienza del framework per la raccolta di informazioni disponibili sulla riqualificazione dei BFs, sviluppato nell’ambito del progetto TIMBRE.
</p>