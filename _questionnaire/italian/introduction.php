<h2>Introduzione</h2>
<p>
    Dalla seconda metà del XIX secolo, il processo di industrializzazione e i mutamenti economici hanno provocato la comparsa di numerosi <u id="brownfield">brownfields (BFs)</u> in tutto il territorio Europeo.
</p>
<p> 
    Per fronteggiare il problema della riqualificazione dei BFs, sono stati sviluppati numerosi network e progetti di ricerca. Tali progetti hanno prodotto un gran numero di metodologie, di framework decisionali, di manuali, di sistemi di supporto alle decisioni, di linee guida, strumenti, ecc., intesi a generare un forte impatto sulla gestione dei BFs, anche se fino ad ora il loro impiego pratico è stato tuttavia limitato. Nello specifico, sono stati sviluppati numerosi framework concettuali, ma troppo spesso questi non considerano tutti gli aspetti legati alla riqualificazione dei BFs.
</p>
<p> 
    Il progetto europeo del VII Programma Quadro TIMBRE – Tailored Improvement for Brownfield Regeneration in Europe – (<a target="blank" href="http://www.timbre-project.eu">www.timbre-project.eu</a>) ha l’obiettivo di supportare gli utilizzatori finali mettendo a disposizione approcci ad hoc per la specifica problematica e per lo specifico obiettivo, nonché strumenti di gestione per la pianificazione e la rigenerazione dei BFs. 
</p>
<p> 
    Uno dei risultati del progetto TIMBRE è lo sviluppo di un framework per la raccolta di informazioni disponibili sulla riqualificazione dei BFs. Tale framework è stato sviluppato partendo dalla valutazione e integrazione dei framework per la riqualificazione dei BFs sviluppati in progetti di ricerca precedenti e considerando gli input e i suggerimenti sulle categorie di informazione  
    (
<u class="c_sp">Pianificazione strategica</u>,
<u class="c_i">Caratterizzazione (preliminare/di dettaglio)</u>,
<u class="c_ra">Analisi di rischio (qualitativa/quantitativa)</u>,
<u class="c_rs">Strategie di bonifica e opzioni alternative</u>,
<u class="c_rt">Valutazione e selezione delle tecnologie di bonifica</u>,
<u class="c_bi">Documentazione riguardante costruzioni e infrastrutture</u>,
<u class="c_d">Decostruzione e riutilizzo di strutture e materiali</u>,
<u class="c_w">Gestione dei rifiuti</u>,
<u class="c_req">Progetto di riqualificazione</u>,
<u class="c_impl">Implementazione, controllo, monitoraggio (ritorno del sito sul mercato)</u>,
<u class="c_sea">Valutazione socio-economica</u>,
<u class="c_ff">Reperimento di fondi e finanziamenti</u> e 
<u class="c_dm">Processo decisionale e comunicazione</u>

) forniti dagli stakeholders. Il framework per la raccolta delle informazioni sulla riqualificazione dei BFs è riportato in Fig. 1.
</p>
